/**
 * Mô hình 3 khối: Tính diện tích, chu vi hình chữ nhật
 * Đầu vào:
 * - Tạo biến chieuDai gán giá trị 12
 * - Tạo biến chieuRong gán giá trị 8
 * Xử lý:
 * - Tạo biến dienTich = chieuDai * chieuRong
 * - Tạo biến chuVi = (chieuDai + chieuRong) * 2
 * Đầu ra:
 * - Đưa ra thông báo diện tích, chu vi hình chữ nhật
 */
var chieuDai = 12;
var chieuRong = 8;
var dienTich = chieuDai * chieuRong;
var chuVi = (chieuDai + chieuRong) * 2;
console.log("Diện tích hình chữ nhật là: " + dienTich);
console.log("Chu vi hình chữ nhật là " + chuVi);